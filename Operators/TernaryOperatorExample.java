package Operators;
public class TernaryOperatorExample {
        public static void main(String[] args) {
            int x = 10;
            int y = 20;
    
            // Using the ternary operator to find the maximum of two numbers
            int max = (x > y) ? x : y;
    
            System.out.println("The maximum of " + x + " and " + y + " is: " + max);
        }
    }
    

