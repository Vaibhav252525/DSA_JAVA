
class Temprature_convert
{
    public static void main (String[] args)
    {
        int cel1 = -7;
        int cel2 = -9;
        
        System.out.printf("47 deg Celsius in Fahrenheit:%.2f\n", (9.0/5*cel1 + 32) );
        System.out.printf("29 deg Celsius in Fahrenheit:%.2f\n", (9.0/5*cel2 + 32) );
        
    }
}

